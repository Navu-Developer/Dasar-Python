# Operasi aritmatika dalam Python
# jumlah, kurang, kali, bagi, sisa bagi, pangkat

a = int(input("Nilai a : "))
b = int(input("Nilai b : "))

# penjumlahan
hasil = a + b
print("a + b = ",hasil)

# pengurangan
hasil = a - b
print("a - b = ",hasil)

# Perkalian
hasil = a * b
print("a x b = ",hasil)

# Pembagian
hasil = a / b
print("a : b = ",hasil) # hasil berupa float

# sisa bagi
hasil1 = a % b
print("a : b = ",hasil,"Bersisa : ",hasil1)

# Pemangkatan
hasil = a ** b
print("a ^ b = ",hasil)
