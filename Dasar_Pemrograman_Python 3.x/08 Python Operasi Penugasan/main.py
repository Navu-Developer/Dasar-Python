# Operasi Penugasan pada Python
# = , += , -= , *= , /= , ** =

# sama dengan
a = 18
print("variabel a telah ditugaskan untuk menyimpan nilai 18")

# tambah dengan
a+=2
print("variabel a telah ditugaskan untuk menambah nilainya dengan nilai 2 manjadi ",a)

# kurang dengan
a-=3
print("variabel a telah ditugaskan untuk mengurangi nilainya dengan nilai 3 menjadi ",a)

# kali dengan
a*=2
print("variabel a telah ditugaskan untuk mengalikan nilainya dengan nilai 2 menjadi ",a)

# bagi dengan
a/=5
print("variabel a telah ditugaskan untuk membagikan nilainya dengan nilai 5 menjadi ",a)

# pangkatkan dengan
a**=2
print("variabel a telah ditugaskan untuk memangkatkan nilainya dengan nilai 2 menjadi ",a)
